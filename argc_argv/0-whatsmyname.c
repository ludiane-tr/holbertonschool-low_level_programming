#include <stdio.h>
#include <stdlib.h>
/**
 * main - Entry point
 *
 * @argc: Number of parameters that go into main
 * @argv: Pointer of array of pointers containing strings entering main
 * Return: Always 0 
 */
int main(int __attribute__((__unused__)) argc, char *argv[])
{
	printf("%s\n", argv[0]);

	return (0);
}
